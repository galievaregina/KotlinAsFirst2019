fun hasDifferentDigits(n: Int): Boolean {
    var a = n
    var s = 0
    var k = n
    var l = 0
    var count = 0
    while (a != 0) {
        a /= 10
        count++
    }
    while (k != 0) {
        s = k % 10
        k /= 10
        if (s != n % 10) break
